#include "ft_printf.h"

size_t	ft_putchar(char c)
{
	write(1, &c, 1);
	return(1);
}
size_t	ft_strlen( const char *s)
{
	size_t	x;

	x = 0;
	while (s[x] != '\0')
		x++;
	return (x);
}
size_t	ft_putstr(char *s)
{
	if (!s)
		s = "(null)";
	write(1, s, ft_strlen(s));
	return (ft_strlen(s));
}

